    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int col = jTable1.getSelectedColumn();
        int fil = jTable1.getSelectedRow();
        String scoreString;
        int score = 0;
        String recordString;
        int record = 0;

        if (!muerto) {
            if (jTable1.getValueAt(fil, col).equals("?")) {
                jTable1.setValueAt(tablaValores[fil][col], fil, col);

                recordString = jLabel4.getText();
                record = Integer.parseInt(recordString);

                if (jTable1.getValueAt(fil, col).equals("X")) {
                    JOptionPane.showMessageDialog(null, "DERROTA");
                    scoreString = jLabel1.getText();
                    score = Integer.parseInt(scoreString);
                    muerto = true;

                    if (score > record) {
                        record = score;
                        jLabel4.setText(String.valueOf(record));
                    }

                } else if (jTable1.getValueAt(fil, col).equals("W")) {
                    scoreString = jLabel1.getText();
                    score = Integer.parseInt(scoreString);
                    score = score * 2;
                    jLabel1.setText(String.valueOf(score));
                } else if (jTable1.getValueAt(fil, col).equals("0")) {
                    scoreString = jLabel1.getText();
                    score = Integer.parseInt(scoreString);
                    score++;
                    jLabel1.setText(String.valueOf(score));
                }
            }
        }

    }//GEN-LAST:event_jTable1MouseClicked


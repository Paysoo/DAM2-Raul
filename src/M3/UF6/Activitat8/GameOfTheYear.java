    public void omplirTaula() {

        for (int i = 0; i < 4; i++) {
            for (int j = 0; j < 4; j++) {
                jTable1.setValueAt("?", i, j);
            }
        }
    }

    public String[][] rellenarValoresReales() {

        String[][] tablaValores = new String[4][4];

        int numX = 2;
        int numW = (int) (Math.random() * 3) + 1;
        int fil = 0;
        int col = 0;

        for (int i = 0; i < numX; i++) {
            fil = (int) (Math.random() * 3);
            col = (int) (Math.random() * 3);
            if (tablaValores[fil][col] == null) {
                tablaValores[fil][col] = "X";
            }
        }

        for (int i = 0; i < numW; i++) {
            do {
                fil = (int) (Math.random() * 3);
                col = (int) (Math.random() * 3);
            } while (tablaValores[fil][col] != null);
            tablaValores[fil][col] = "W";
        }

        for (int i = 0; i < 4; i++) {
            for (int j = 0; j < 4; j++) {
                if (tablaValores[i][j] == null) {
                    tablaValores[i][j] = "0";
                }
            }
        }

        return tablaValores;

    }


